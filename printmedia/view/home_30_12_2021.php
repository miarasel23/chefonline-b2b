<script>
    function sendContact() {

        var valid;
        valid = validateContact();
        if (valid) {
            jQuery.ajax({
                url: "promotion.php",
                data: 'name=' + $("#name").val() + '&email=' + $("#email").val() + '&phone=' + $(phone).val(),
                type: "POST",
                success: function(data) {
                    $("#mail-status").html(data);
                    setTimeout(function() {
                        $('#promotion').modal('hide');
                    }, 3000);
                },
                error: function() {}
            });
        }
    }

    function validateContact() {
        var valid = true;



        $('input').keydown(function(e) {
            if (e.keyCode == 32 && e.target.selectionStart === 0) {
                return false;
            }
        });

        if (!$("#name").val()) {
            $("#name-info").html("Enter your name");
            $("#name").css('border', '2px solid #ff0000');
            valid = false;
        }

        // if(!$("#email").val()) {
        //     $("#email-info").html("Enter your email");
        //     $("#email").css('border','2px solid #ff0000');
        //     valid = false;
        // }

        var hasError = false;
        var emailReg = /^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/;

        var emailaddressVal = $("#email").val();
        if (emailaddressVal == '') {
            $("#email-info").html("Enter your email");
            $("#email").css('border', '2px solid #ff0000');
            hasError = true;
        } else if (!emailReg.test(emailaddressVal)) {
            $("#email-info").html("Enter valid email");
            $("#email").css('border', '2px solid #ff0000');
            hasError = true;
        }


        if (!$("#phone").val()) {
            $("#phone-info").html("Enter your phone no");
            $("#phone").css('border', '2px solid #ff0000');
            valid = false;
        }

        return valid;
    }
</script>

<script type="text/javascript">
    function subscribe(e) {

        e.preventDefault();
        var email = document.getElementById('subscription_email').value;
        var pattern = /^([a-zA-Z0-9_.-])+@([a-zA-Z0-9_.-])+\.([a-zA-Z])+([a-zA-Z])+/;

        if (pattern.test(email)) {
            document.getElementById('return_msg').innerHTML = 'Please wait ...';
            var xmlhttp;
            if (window.XMLHttpRequest) {
                xmlhttp = new XMLHttpRequest();
            } else {
                xmlhttp = new ActiveXObject("Microsoft.XMLHTTP");
            }
            xmlhttp.onreadystatechange = function() {
                if (xmlhttp.readyState == 4 && xmlhttp.status == 200) {
                    if (xmlhttp.responseText == 1) {
                        document.getElementById('return_msg').innerHTML = 'You are successfully subscribed.';
                    } else if (xmlhttp.responseText == 2) {
                        document.getElementById('return_msg').innerHTML =
                        'Your email already in subscription list.';
                    } else {
                        document.getElementById('return_msg').innerHTML = 'Subscription Failed. Please Try Later';
                    }
                    document.getElementById('subscription_email').value = "";
                }
            }
            xmlhttp.open("POST", "subscribe.php", true);
            xmlhttp.setRequestHeader("Content-type", "application/x-www-form-urlencoded");
            xmlhttp.send("email=" + email);
        } else {
            document.getElementById('return_msg').innerHTML = "Invalid E-mail Address";
        }

    }
</script>